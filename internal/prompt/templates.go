package prompt

// loadDefaults populates the registry with the canonical v3 templates.
// Each template is designed for a specific scenario in the Agentic RAG pipeline.
func (r *Registry) loadDefaults() {
	r.templates[ScenarioSystem] = upgradeDefaultTemplateV3(systemBase)
	r.templates[ScenarioIntent] = upgradeDefaultTemplateV3(intentClassifier)
	r.templates[ScenarioRewrite] = upgradeDefaultTemplateV3(queryRewriter)
	r.templates[ScenarioPlan] = upgradeDefaultTemplateV3(reactPlanner)
	r.templates[ScenarioGenerateGeneric] = upgradeDefaultTemplateV3(generateGeneric)
	r.templates[ScenarioGenerateCompare] = upgradeDefaultTemplateV3(generateCompare)
	r.templates[ScenarioGenerateDiagnose] = upgradeDefaultTemplateV3(generateDiagnose)
	r.templates[ScenarioGeneratePolicy] = upgradeDefaultTemplateV3(generatePolicy)
	r.templates[ScenarioReflect] = upgradeDefaultTemplateV3(reflectionChecker)
	r.templates[ScenarioClarify] = upgradeDefaultTemplateV3(clarifyGuide)
	r.templates[ScenarioSummarize] = upgradeDefaultTemplateV3(conversationSummarizer)
	r.templates[ScenarioEvalJudge] = upgradeDefaultTemplateV3(evalJudge)

	for _, tmpl := range r.templates {
		r.versions[tmpl.Scenario] = tmpl.Version
		r.storeLocked(tmpl)
	}
}

